
public class Pattern 
{
	private String name;
	private String globally;
	private String before;
	private String after;
	private String between;
	private String afterUntil;
	
	public Pattern()
	{
		
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGlobally() {
		return globally;
	}

	public void setGlobally(String globally) {
		this.globally = globally;
	}

	public String getBefore() {
		return before;
	}

	public void setBefore(String before) {
		this.before = before;
	}

	public String getAfter() {
		return after;
	}

	public void setAfter(String after) {
		this.after = after;
	}

	public String getBetween() {
		return between;
	}

	public void setBetween(String between) {
		this.between = between;
	}

	public String getAfterUntil() {
		return afterUntil;
	}

	public void setAfterUntil(String afterUntil) {
		this.afterUntil = afterUntil;
	}


	
}
